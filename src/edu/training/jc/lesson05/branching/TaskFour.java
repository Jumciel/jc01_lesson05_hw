public class TaskFour {

	public static void main(String[] args) {
		int a = 8;
		int b = 7;

		if (a == b) {
			System.out.println("Числа a и b равны\n" + a + " = " + b);
		} else {
			System.out.println("Числа a и b имеют разные значения\n" + a + " ≠ " + b);
		}

	}

}
